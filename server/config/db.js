import mongoose from 'mongoose';

const connectDB = async (retries = 5) => {
  try {
    console.log('Attempting to connect to MongoDB...');

    // Add connection options for better reliability
    const conn = await mongoose.connect(process.env.MONGO_URI, {
      serverSelectionTimeoutMS: 30000, // 30 seconds
      socketTimeoutMS: 45000, // 45 seconds
      family: 4 // Use IPv4, skip trying IPv6
    });

    console.log(`âœ… MongoDB Connected Successfully: ${conn.connection.host}`);
  } catch (error) {
    console.error('âŒ MongoDB connection failed.');
    console.error('Error Details:', error.message);

    if (retries > 0) {
      console.log(`ðŸ”„ Retrying connection in 5 seconds... (${retries} retries left)`);
      await new Promise(resolve => setTimeout(resolve, 5000));
      return connectDB(retries - 1);
    } else {
      console.error('âŒ FATAL: Could not connect to MongoDB after multiple attempts.');
      process.exit(1);
    }
  }
};

// Handle connection events
mongoose.connection.on('connected', () => {
  console.log('ðŸ”„ Mongoose connected to MongoDB');
});

mongoose.connection.on('error', (err) => {
  console.error('âŒ Mongoose connection error:', err);
});

mongoose.connection.on('disconnected', () => {
  console.log('ðŸ”„ Mongoose disconnected');
});

// Handle application termination
process.on('SIGINT', async () => {
  await mongoose.connection.close();
  process.exit(0);
});

