import React from "react";
import clsx from "clsx";

const Container = ({
  children,
  className,
  size = "default",
  padding = true,
  fluid,
  ...props
}) => {
  const maxWidths = {
    sm: "max-w-screen-sm",
    md: "max-w-screen-md",
    lg: "max-w-screen-lg",
    xl: "max-w-screen-xl",
    "2xl": "max-w-screen-2xl",
    default: "max-w-7xl",
  };

  return (
    <div
      className={clsx(
        "w-full mx-auto",
        !fluid && maxWidths[size],
        padding && "px-4 sm:px-6 lg:px-8",
        className,
      )}
      {...props}
    >
      {children}
    </div>
  );
};

Container.displayName = "Container";
export { Container };
export default Container;
